    private void btnPaySalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPaySalaryActionPerformed
        
         /**
         * this button designed for pay doctors salary
         */
        String row = listDoctors.getSelectedValue();
        
        String str[] = row.split(Commons.SPLITTER);
        
        String drName = str[0];
        
        if(drName != null) {
            
           JOptionPane.showMessageDialog(rootPane, "Salary of Dr. " + drName + " payed");
        }
    }//GEN-LAST:event_btnPaySalaryActionPerformed

