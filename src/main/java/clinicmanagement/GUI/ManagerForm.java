    private void btnRemoveSectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveSectionActionPerformed
        
        /**
         * this button designed for delete a section from file sections
         */ 
            
        int selectedIndex = listSections.getSelectedIndex();
        
        if(selectedIndex != -1) {
            this.txtFileManagerSec.deleteRow(selectedIndex);
            modelSec.remove(selectedIndex);
        }
       listSections.setModel(modelSec);
        
    }//GEN-LAST:event_btnRemoveSectionActionPerformed

